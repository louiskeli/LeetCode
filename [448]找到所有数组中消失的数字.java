class Solution {
    public List<Integer> findDisappearedNumbers(int[] nums) {
        List<Integer> outNumber = new LinkedList<>();

        for (int i = 0 ; i < nums.length ; i++){
            int num = nums[i] > 0 ? nums[i] : nums[i] * -1;
            if (nums[num - 1] > 0) {
                nums[num - 1] *= -1;
            }
        }

        for (int i = 1 ; i <= nums.length ; i++){
            if (nums[i - 1] > 0 ){
                outNumber.add(i);
            }
        }

        return outNumber;
    }
}
