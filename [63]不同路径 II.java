class Solution {
    public int uniquePathsWithObstacles(int[][] obstacleGrid) {
        int m = obstacleGrid.length;
        int n = obstacleGrid[0].length;
        //ç”¨æ•°ç»„ansè®°å½•åˆ°è¾¾æ¯ä¸ªä½ç½®çš„è·¯å¾„æ•°
        int[][] ans = new int[m][n];
        //å¯¹è¾¹ç•Œè¿›è¡Œç•Œå®šï¼Œå’Œä¸Šä¸€é¢˜ä¸åŒçš„æ˜¯ï¼šç¬¬ä¸€è¡Œå’Œç¬¬ä¸€åˆ—obstacleGridä½ç½®åªè¦å‡ºç°1ï¼Œé‚£ä¹ˆåé¢ansä½ç½®å…¨æ˜¯0
        for (int i = 0 ; i < m; i++) {
            if (obstacleGrid[i][0] == 1) break;
            else ans[i][0] = 1;
        }
        for (int i = 0; i < n ; i++) {
            if (obstacleGrid[0][i] == 1) break;
            else ans[0][i] = 1;
        }
        //å¯¹é™¤è¾¹ç•Œå¤–çš„ä½ç½®è·¯å¾„æ•°è¿›è¡Œç»Ÿè®¡ï¼šå¦‚æœobstacleGridä½ç½®ä¸Šæ˜¯1åˆ™è¡¨æ˜è¯¥ä½ç½®æœ‰éšœç¢ç‰©ï¼Œansä½ç½®è·¯å¾„ä¸º0
        //obstacleGridä½ç½®ä¸ä¸º0ï¼Œåˆ™ansä½ç½®ç­‰äºä¸Šè¾¹çš„ansä½ç½®è·¯å¾„æ•°åŠ ä¸Šå·¦è¾¹çš„è·¯å¾„æ•°
        for (int i = 1; i < m ; i++) {
            for (int j = 1; j < n; j++) {
                if (obstacleGrid[i][j] == 1) ans[i][j] = 0;
                else ans[i][j] = ans[i - 1][j] + ans[i][j - 1];
            }
        }
        return ans[m - 1][n - 1];
    }
}
